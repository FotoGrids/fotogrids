/**
 * Build script: YAML (source) → JSON outputs.
 *
 * Produces three files:
 *
 *   src/config/loading-icons.json
 *     SVG strings keyed by icon name, with SMIL tags stripped.
 *     Used by PHP (fotogrids_get_loading_icon_svg) to inline markup into the
 *     frontend page. SMIL stripped because animation is driven by WAAPI.
 *
 *   src/config/loading-icons-smil.json
 *     SVG strings keyed by icon name, with SMIL tags intact.
 *     Used by the wp-admin UI (fetched by loading-icons.js) so admin previews
 *     animate via SMIL without needing the WAAPI runtime.
 *
 *   src/config/loading-icons-waapi.json
 *     Self-contained WAAPI animate function strings keyed by icon name.
 *     PHP reads the selected icon's entry and emits it verbatim as raw JS
 *     in the window.fotogridsLoadingIcons inline script global.
 *     Generated by running smil-to-waapi.js as a subprocess.
 *
 * Run as part of npm run build and npm run dev.
 * Source file already contains __FG_ID__ placeholders; no injection in build.
 */
const fs   = require('fs');
const path = require('path');
const yaml = require('js-yaml');
const { execSync } = require('child_process');

const ROOT            = path.join(__dirname, '..');
const YAML_PATH       = path.join(ROOT, 'src/config/loading-icons.yaml');
const JSON_FALLBACK   = path.join(ROOT, 'src/config/loading-icons.json');
const JSON_OUT        = path.join(ROOT, 'src/config/loading-icons.json');
const JSON_SMIL_OUT   = path.join(ROOT, 'src/config/loading-icons-smil.json');
const CONVERTER       = path.join(__dirname, 'smil-to-waapi.js');

function loadSource() {
  if (fs.existsSync(YAML_PATH)) {
    try {
      const content = fs.readFileSync(YAML_PATH, 'utf8');
      const data = yaml.load(content);
      if (data && typeof data === 'object' && !Array.isArray(data)) return data;
    } catch (e) {
      console.error('loading-icons: YAML parse failed:', e.message);
      process.exit(1);
    }
  }
  if (fs.existsSync(JSON_FALLBACK)) {
    const content = fs.readFileSync(JSON_FALLBACK, 'utf8');
    return JSON.parse(content);
  }
  console.error('loading-icons: neither loading-icons.yaml nor loading-icons.json found');
  process.exit(1);
}

/**
 * Strips SMIL animation elements from an SVG string.
 * Mirrors the stripSmil() logic in smil-to-waapi.js.
 */
function stripSmil(svg) {
  return svg
    .replace(/<animate(?:Transform)?\s[^>]*\/>/g, '')
    .replace(/<animate(?:Transform)?[\s>][\s\S]*?<\/animate(?:Transform)?>/g, '')
    .replace(/\s*id=['"][^'"]*fg_ia_[^'"]*['"]/g, '')
    .replace(/[ \t]+\n/g, '\n')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

function main() {
  const data = loadSource();
  const stripped = {};
  const smil = {};
  for (const [key, value] of Object.entries(data)) {
    const raw = typeof value === 'string' ? value : (Array.isArray(value) ? value.join('') : String(value));
    stripped[key] = stripSmil(raw);
    smil[key] = raw;
  }

  // Frontend: SMIL stripped, animations driven by WAAPI.
  fs.writeFileSync(JSON_OUT, JSON.stringify(stripped, null, 2), 'utf8');
  console.log('loading-icons: wrote', JSON_OUT);

  // Admin: SMIL intact, used by wp-admin UI previews via fetch().
  fs.writeFileSync(JSON_SMIL_OUT, JSON.stringify(smil, null, 2), 'utf8');
  console.log('loading-icons: wrote', JSON_SMIL_OUT);

  // Run the SMIL→WAAPI converter to produce loading-icons-waapi.json.
  // The converter also regenerates loading-icons-waapi.js (dev artifact) and
  // waapi-compare.html — both are dev-only outputs, never shipped to dist.
  try {
    execSync(`node "${CONVERTER}"`, { stdio: 'inherit', cwd: ROOT });
  } catch (e) {
    console.error('loading-icons: smil-to-waapi.js failed');
    process.exit(1);
  }
}

main();
